import java.sql.Timestamp;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_EMPTY)
public class Book implements java.io.Serializable {

	private static final long serialVersionUID = 3041647354928870900L;

	private String id;
	private String name;
	private String isbn;
	private Timestamp revisionDateTime;
	private int numPages;
	private String description;

	public Book() {
		// noop
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIsbn() {
		return isbn;
	}

	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}

	public Timestamp getRevisionDateTime() {
		return revisionDateTime;
	}

	public void setRevisionDateTime(Timestamp revisionDateTime) {
		this.revisionDateTime = revisionDateTime;
	}

	public int getNumPages() {
		return numPages;
	}

	public void setNumPages(int numPages) {
		this.numPages = numPages;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
